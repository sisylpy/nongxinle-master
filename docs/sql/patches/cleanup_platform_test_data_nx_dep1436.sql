-- =============================================================================
-- 删除「三店 1436」旧订单 — 分步执行（一次只跑一段）
-- 重要：先停掉 Tomcat / 后端，再执行，否则可能一直卡住删不动
-- =============================================================================

USE nongxinle;


-- -----------------------------------------------------------------------------
-- 第 0 步：如果之前删到一半卡住了，先执行这一句
-- -----------------------------------------------------------------------------
ROLLBACK;


-- -----------------------------------------------------------------------------
-- 第 1 步：看还剩多少（心里有数）
-- -----------------------------------------------------------------------------
SELECT 'assign待删' AS 项目, COUNT(*) AS 数量
FROM nx_platform_order_assign
WHERE nx_poa_market_id = 1 AND nx_poa_department_id = 1436 AND nx_poa_assign_mode = 'PLATFORM'
UNION ALL
SELECT 'fulfillment待删', COUNT(*)
FROM nx_platform_order_fulfillment
WHERE nx_pof_department_id = 1436;


-- -----------------------------------------------------------------------------
-- 第 2 步：先删 fulfillment（必须先删这个，否则 assign 删不动）
-- 跑完看 Affected rows，应该 > 0
-- -----------------------------------------------------------------------------
DELETE FROM nx_platform_order_fulfillment
WHERE nx_pof_department_id = 1436;


-- -----------------------------------------------------------------------------
-- 第 3 步：删切换日志
-- -----------------------------------------------------------------------------
DELETE FROM nx_supplier_switch_log
WHERE nx_ssl_department_id = 1436;


-- -----------------------------------------------------------------------------
-- 第 4 步：删 assign（第 2 步成功后，这一步应该很快）
-- -----------------------------------------------------------------------------
DELETE FROM nx_platform_order_assign
WHERE nx_poa_market_id = 1
  AND nx_poa_department_id = 1436
  AND nx_poa_assign_mode = 'PLATFORM';


-- -----------------------------------------------------------------------------
-- 第 5 步：删 nx 订单行（三店自己的订单，gb 字段是 -1）
-- -----------------------------------------------------------------------------
DELETE FROM nx_department_orders
WHERE nx_DO_department_id = 1436;


-- -----------------------------------------------------------------------------
-- 第 6 步：检查结果
-- 三店1436剩余 → 0
-- GB57待分配   → 1
-- -----------------------------------------------------------------------------
SELECT '三店1436剩余' AS 说明, COUNT(*) AS 数量
FROM nx_platform_order_assign
WHERE nx_poa_market_id = 1 AND nx_poa_department_id = 1436
UNION ALL
SELECT 'GB57待分配', COUNT(*)
FROM nx_platform_order_assign
WHERE nx_poa_market_id = 1
  AND nx_poa_department_id = 57
  AND nx_poa_assign_status = 'PENDING';
